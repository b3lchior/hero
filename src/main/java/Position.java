public class Position {
    private int x;
    private int y;

    public Position(int x_ , int y_) {
        x = x_;
        y = y_;
    }

    public int getX() { return x;}                                  //getters and setters
    public void setX(int x) { this.x = x;}
    public int getY() { return y;}
    public void setY(int y) { this.y = y;}

}
